		<h1>APA Contribution</h1>
		
		<p>There are a number of ways non-participants in the APA WG can contribute to the work. If you wish to be an active contributer, however, you should <a href="participation">join the Working Group</a>.</p>
		
		<section id="comments">
			<h2>Public comments</h2>
			<p>The APA WG accepts public comments on publications on the <a href="http://lists.w3.org/Archives/Public/public-apa/">public-apa</a> list. This is primarily used for input on formal publications. </p>
		</section>
		
		<section id="github">
			<h2>Github issues and pull requests</h2>
			<p>Much of the technical work is done in the <a href="https://github.com/w3c/apa/">W3C PFWG repository on GitHub</a>. A <a href="http://github.com/">GitHub account</a> is required for the following functionality. In addition to using the comments list described above to submit input, you may:</p>
			<ul>
				<li><a href="https://help.github.com/articles/watching-repositories/">Watch the repository</a>;</li>
				<li><a href="https://guides.github.com/features/issues/">File issues</a>;</li>
				<li>Submit <a href="https://help.github.com/articles/using-pull-requests/">pull requests</a>.</li>
			</ul>
		</section>
		
		<section id="lists">
			<h2>Mailing list discussions</h2>
			<p>If you would like to monitor the work of the APA WG and contribute on-line, you may subscribe to the following mailing lists. You will not be considered a formal
  participant but may comment and contribute. The APA WG uses the following mailing lists:</p>
			<ul>
  				<li><a href="http://lists.w3.org/Archives/Public/wai-xtech/">wai-xtech@w3.org</a> is used for technical discussion. <a href="mailto:wai-xtech-request@w3.org?subject=subscribe">Subscribe to wai-xtech</a> by sending mail to wai-xtech-request@w3.org with the subject "subscribe" or <a href="mailto:wai-xtech@w3.org">Post to wai-xtech</a> by sending mail to wai-xtech@w3.org.</li>
  				<li>The <a href="http://www.w3.org/WAI/IG/">WAI Interest Group</a> and its mailing lists is used for announcements of WG activity and to coordinate specification review.</li>
				<li><a href="http://lists.w3.org/Archives/Public/public-apa/">public-apa@w3.org</a> is used for technical discussion of the WG.</li>
				<li><a href="http://lists.w3.org/Archives/Public/public-apa-admin/">public-apa-admin@w3.org</a> is used for administrative coordination of the WG.</li>
			</ul>
