		<h1>APA Task Forces</h1>
		
		<p>The APA Working Group conducts some of its work via the following task forces. See each task force page for details about specific work and how to join.</p>
		
		<section id="coga">
		<h2>Cognitive and Learning Disabilities Accessibility Task Force</h2>
		<p>The <a href="http://www.w3.org/WAI/GL/task-forces/coga/">Cognitive and Learning Disabilities Accessibility Task Force</a> will develop draft proposed guidance and techniques to make web content, content authoring, and user agent implementation accessible and more useable by people with cognitive and learning disabilities. </p>
		</section>
		
		<section id="personalization">
			<h2>Personalization Task Force</h2>
			<p>The <a href="personalization/">Personalization Task Force</a> will refine the <a href="https://www.w3.org/TR/personalization-semantics-1.0/">Personalization Semantics</a> specification, in consultation with the ARIA Working Group. The task force provides a focused forum for this work while the Working Group continues its work on ARIA development. Personalization Semantics was listed in the <a href="/WAI/ARIA/charter">ARIA charter</a> as "User Context" but was renamed as work progressed.</p>
		</section>
		
		<section id="rqtf">
		<h2>Research Questions Task Force</h2>
		<p>The <a href="research-questions/">Research Questions Task Force (RQTF)</a> works to identify accessibility knowledge gaps and barriers in emerging and future web technologies, and to identify research findings, researchers, and research opportunities to inform and fill those gaps where possible.</p>
		</section>
		
		<section id="pronunciation">
			<h2>Spoken Pronunciation Task Force</h2>
			<p>The <a href="pronunciation/">Spoken Pronunciation Task Force</a> will develop normative specifications and best practices guidance collaborating with other W3C groups as appropriate, to provide for proper pronunciation in HTML content when using text to speech (TTS) synthesis.</p>
		</section>
		
		<section id="css-a11y">
			<h2>CSS Accessibility Task Force</h2>
			<p>The <a href="css-a11y/">CSS Accessibility Task Force</a> is a joint task force of the APA WG, the <a href="/WAI/ARIA/">Accessible Rich Internet Applications Working Group</a>, and the <a href="/Style/CSS/">Cascading Style Sheets Working Group</a>. It will focus effort on identifying accessibility problems that can be created by use of Cascading Style Sheets (CSS), and to develop solutions.</p>
		</section>

		<section id="payments">
		<h2>Web Payments Accessibility Sub-Group</h2>
		<p>The Web Payments Accessibility Sub-Group explores accessibility use cases and requirements to develop as a deliverable, Payment Accessibility User Requirements. It coordinates with the <a href="https://www.w3.org/Payments/WG/">Web Payments Working Group</a>.</p>
		</section>
		
