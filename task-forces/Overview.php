		<section id="spec-review">
		<h2>Specification Accessibility Task Force</h2>
		<p>The <a href="http://www.w3.org/WAI/PF/Group/spec-review-tf">Specification Accessibility Task Force</a> assists the Working Group with accessibility review of W3C specifications. Currently this work is done by the working group as a whole.</p>
